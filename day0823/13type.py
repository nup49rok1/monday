x = 234
y = 3.145
z = True
msg = 'F'
message = '금요일'


print(x,type(x)) #int 정수
print(y,type(y)) #float 실수
print(z,type(z)) #bool 불
print(msg,type(msg)) #str 문자
print(message,type(message)) #str문자

'''
234 <class 'int'>
3.145 <class 'float'>
True <class 'bool'>
F <class 'str'>
금요일 <class 'str'>
'''